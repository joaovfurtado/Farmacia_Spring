    public void verificaMenu() {
        switch (Propriedades.getUser().getPerm()) {
            case "e":
                btnEstoq.setEnabled(true);
                btnCaixa.setEnabled(false);
                break;
            case "c":
                btnEstoq.setEnabled(false);
                btnCaixa.setEnabled(true);
                break;
            case "a":
                btnEstoq.setEnabled(true);
                btnCaixa.setEnabled(true);
                break;
        }
    }

